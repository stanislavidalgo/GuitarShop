        <div class="teacher1">
            <img src="images/teachers/david-gilmour.jpg" width="280px" alt="david-gilmour">
            <p class="mentorName">David Gilmour</p>
            <p class="strenght">TOP STRENGTH: MATURE SIMPLICITY</p>
            <p class="bio">David Gilmour is the voice and guitar of the prog rock kings Pink Floyd. Like everyone on this list, he's won all the awards. He's known for his simple but huge riffs and ambient chords, full of blues phrasing, note bends, and plenty of sustain. He doesn't get a lot of credit for it, but he can play many other instruments just as well.</p>
        </div>
        <div class="teacher2">
            <img src="images/teachers/eddie-van-halen.jpg" width="280px" alt="eddie-van-halen">
            <p class="mentorName">Eddie Van Halen</p>
            <p class="strenght">TOP STRENGTH: TAPPING & WHAMMY BAR</p>
            <p class="bio">Eddie Van Halen often lands at number one in these lists. He's that good. He credits Eric Clapton as his main influence but says he's closer to Jimmy Page in style. He's the kind of player that has been tapped by other top acts like Michael Jackson to play backing tracks for them.</p>
        </div>
        <div class="teacher3">
            <img src="images/teachers/stevie-ray-vaughan.jpg" width="280px" alt="stevie-ray-vaughan">
            <p class="mentorName">Stevie Ray Vaughan</p>
            <p class="strenght">TOP STRENGTH: STRING RAKING</p>
            <p class="bio">Stevie Ray Vaughan is one of the most influential guitarists in blues music, leading the revival of blues rock in the 1980's. Any list of best guitarists will undoubtedly feature this great within their top 10, or they're wrong. He credits much of his style as coming from Lonnie Mack. It's said that regardless of genre, his style is so completely unique that no other guitarist has ever sounded like him.</p>
        </div>
        <div class="teacher4">
            <img src="images/teachers/guthrie-govan.jpg" width="280px" alt="gurthie-govan">
            <p class="mentorName">Gurthie Govan</p>
            <p class="strenght">TOP STRENGTH: ALL-AROUND TECHNICAL VIRTUOSO</p>
            <p class="bio">Guthrie Govan is the best guitarist, period. As amazing as others are, this guy edges them out by not only having the topmost technical ability but can use it in any genre. There are no limits to Govan's abilities. He shares the love, spending much of his time as a guitar clinician, teaching in masterclasses around the world.</p>
        </div>
